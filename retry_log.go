package retry

/////////////////////////////////////////////////////////////////////////////
// Logger
/////////////////////////////////////////////////////////////////////////////
type Logger func(...interface{})

/////////////////////////////////////////////////////////////////////////////
// Debug Logger
/////////////////////////////////////////////////////////////////////////////
var defaultDebugLogger Logger = nil

func SetDebugLogger(logger Logger) {
	defaultDebugLogger = logger
}

/////////////////////////////////////////////////////////////////////////////
// Error Logger
/////////////////////////////////////////////////////////////////////////////
var defaultErrorLogger Logger = nil

func SetErrorLogger(logger Logger) {
	defaultErrorLogger = logger
}
