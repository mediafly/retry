package retry

import (
	"time"
)

/////////////////////////////////////////////////////////////////////////////
// Callbacks
/////////////////////////////////////////////////////////////////////////////
type DoCallback func() (Result, error)
type CancelCallback func() error

/////////////////////////////////////////////////////////////////////////////
// Options
/////////////////////////////////////////////////////////////////////////////
type Options struct {
	Tag         interface{}
	Deadline    time.Time
	MaxAttempts uint32
	MaxDelay    time.Duration
	Do          DoCallback
	Cancel      CancelCallback
	Log         Logger
}
