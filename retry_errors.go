package retry

/////////////////////////////////////////////////////////////////////////////
// CancelledError
/////////////////////////////////////////////////////////////////////////////
type CancelledError struct {
	Retry Retry
	Err   error
}

func (e *CancelledError) Error() string {
	return "cancelled"
}

/////////////////////////////////////////////////////////////////////////////
// DeadlineError
/////////////////////////////////////////////////////////////////////////////
type DeadlineError struct {
	Retry Retry
	Err   error
}

func (e *DeadlineError) Error() string {
	return "deadline"
}
